# x: 2
# y: 2

# Введите радиус: 1 
# Монетки в области нет
import math

def is_inside_circle(x_point, y_point, radious_value):
    result = math.sqrt(x_point ** 2 + y_point ** 2)
    if result <= radious_value:
        print('Монетка где-то рядом.')
    else:
        print('Монетки в области нет.')

x_coord = float(input('Введите координату x: '))
y_coord = float(input('Введите координату y: '))
radius = float(input('Введите радиус: '))


is_inside_circle(x_coord, y_coord, radius)