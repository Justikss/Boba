# Даны два действительных числа x и y и радиус r. Напишите функцию, которая проверяет, лежит ли точка с 
# координатами (x; y) внутри круга с радиусом r, включая его границу. 
# Координаты центра круга — (0; 0). Если точка принадлежит кругу, выведите сообщение: «Монетка где-то рядом». 
# В другом случае выведите сообщение: «Монетки в области нет». 

# Пример работы программы 1
# Введите координаты монетки:

# x: 0.5
# y: 0.5

# Введите радиус: 1 
# Монетка где-то рядом

# Пример работы программы 2
